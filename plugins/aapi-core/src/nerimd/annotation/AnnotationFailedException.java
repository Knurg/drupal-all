package nerimd.annotation;

public class AnnotationFailedException extends RuntimeException {
	public static final long serialVersionUID = 545424327612194934l;
	
	public AnnotationFailedException() {
	}

	public AnnotationFailedException(String message) {
		super(message);
	}

	public AnnotationFailedException(Throwable cause) {
		super(cause);
	}

	public AnnotationFailedException(String message, Throwable cause) {
		super(message, cause);
	}

}
